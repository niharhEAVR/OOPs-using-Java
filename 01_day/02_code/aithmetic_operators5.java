public class aithmetic_operators5 {
    public static void main(String[] args) {
        int a = 7;
        int b = 5;

        a+=2; // it adds then assigns the value

        System.out.println(a+b);
        System.out.println(a-b);
        System.out.println(a*b);
        System.out.println(a/b);
        System.out.println(a%b);
        System.out.println(a++);
        System.out.println(a);
    }
}
